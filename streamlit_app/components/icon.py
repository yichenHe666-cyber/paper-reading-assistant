SIZE_MAP = {
    "xs": "12px",
    "sm": "14px",
    "md": "16px",
    "lg": "20px",
    "xl": "24px",
    "2xl": "32px",
}


def icon(name: str, size: str = "md", color: str = None) -> str:
    """Return a Font Awesome icon as HTML string.

    Args:
        name: Icon key from ICON_MAP.
        size: Size variant (xs/sm/md/lg/xl/2xl).
        color: CSS color value or None for default.

    Returns:
        HTML <i> tag string for the icon.
    """
    if name not in ICON_MAP:
        return f"<!-- Icon '{name}' not found -->"

    size_cls = SIZE_MAP.get(size, "16px")
    color_style = f"color: {color};" if color else ""

    return f'<i class="fas {ICON_MAP[name]}" style="font-size: {size_cls}; {color_style}"></i>'


def icon_title(title: str, icon_name: str = None, size: str = "sm", heading: str = "h3") -> str:
    """Return a markdown string with icon + title for st.markdown with unsafe_allow_html=True.

    Args:
        title: The title text.
        icon_name: Icon key from ICON_MAP.
        size: Icon size variant.
        heading: HTML heading tag (h1-h6).

    Returns:
        Markdown HTML string.
    """
    icon_html = icon(icon_name, size=size) if icon_name else ""
    return f"<{heading}>{icon_html} {title}</{heading}>"


def icon_caption(text: str, icon_name: str = None, size: str = "xs") -> str:
    """Return a markdown string with icon + caption text.

    Args:
        text: The caption text.
        icon_name: Icon key from ICON_MAP.
        size: Icon size variant.

    Returns:
        Markdown HTML string.
    """
    icon_html = icon(icon_name, size=size) if icon_name else ""
    return f"<p style='font-size:0.875rem;color:var(--color-text-secondary);'>{icon_html} {text}</p>"